import Foundation
import UIKit


extension UIColor {
    var black25A: UIColor {
        return UIColor(red: 1, green: 1, blue: 1, alpha: 0.25)
    }
    
    var black25: UIColor {
        return UIColor().hexColor(hex: "#404040", alpha: 1.0)
    }
    
    var black0: UIColor {
        return UIColor(white: 0.0, alpha: 0.0)
    }
    
    var black82: UIColor {
        return UIColor(white: 0.0, alpha: 0.82)
    }
    
    var black: UIColor {
        return UIColor(white: 0.0, alpha: 1.0)
    }
}
